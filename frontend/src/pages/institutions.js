export const searchInstitutions = (query) => {
  if (!query || query.length < 2) return [];
  const q = query.toLowerCase().trim();

  // Check alias first
  if (ALIASES[q]) return [ALIASES[q]];

  const results = NIGERIAN_INSTITUTIONS.filter(inst =>
    inst.toLowerCase().includes(q)
  );

  // Sort: exact start matches first, then contains
  results.sort((a, b) => {
    const aStart = a.toLowerCase().startsWith(q) ? 0 : 1;
    const bStart = b.toLowerCase().startsWith(q) ? 0 : 1;
    return aStart - bStart;
  });

  return results.slice(0, 8);
};
